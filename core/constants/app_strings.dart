  static String getTimeString(int minutes) {
    if (minutes < 60) {
      return '$minutes $minutes';
    } else {
      final hours = minutes ~/ 60;
      final remainingMinutes = minutes % 60;
      if (remainingMinutes == 0) {
        return '$hours $hours';
      } else {
        return '$hours $hours و $remainingMinutes $minutes';
      }
    }
  }

